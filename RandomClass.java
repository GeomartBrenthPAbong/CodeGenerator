public class RandomClass {

	private String str;
	private int n;
	private boolean b;

    //----------------------------------------------|
    // Setter(s)
    //----------------------------------------------|

	public void setStr(String str) {
		this.str = str;
	}

	public void setN(int n) {
		this.n = n;
	}

	public void setB(boolean b) {
		this.b = b;
	}

    //----------------------------------------------|
    // Getter(s)
    //----------------------------------------------|

	public String getStr() {
		return this.str;
	}

	public int getN() {
		return this.n;
	}

	public boolean getB() {
		return this.b;
	}
}